
public class Task1_Inheritance_Student {
	
	String ID;
	String Name;
	
	public Task1_Inheritance_Student(String iD, String name) {
		ID = iD;
		Name = name;
	}
	
	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

}